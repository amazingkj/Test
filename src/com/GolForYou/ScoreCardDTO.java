package com.GolForYou;

public class ScoreCardDTO {
	private String s_id;
	private String s_location;
	private int s_putting;
	private int s_range;
	private int s_handicap;
	private String s_date;
	public String getS_id() {
		return s_id;
	}
	public void setS_id(String s_id) {
		this.s_id = s_id;
	}
	public String getS_location() {
		return s_location;
	}
	public void setS_location(String s_location) {
		this.s_location = s_location;
	}
	public int getS_putting() {
		return s_putting;
	}
	public void setS_putting(int s_putting) {
		this.s_putting = s_putting;
	}
	public int getS_range() {
		return s_range;
	}
	public void setS_range(int s_range) {
		this.s_range = s_range;
	}
	public int getS_handicap() {
		return s_handicap;
	}
	public void setS_handicap(int s_handicap) {
		this.s_handicap = s_handicap;
	}
	public String getS_date() {
		return s_date;
	}
	public void setS_date(String s_date) {
		this.s_date = s_date;
	}
	
}
